var Debounce = (fn, t) => {
  const delay = t || 25;
  let timer;
  return function() {
    const args = arguments;
    if (timer) {
      clearTimeout(timer);
    }
    timer = setTimeout(() => {
      timer = null;
      fn.apply(this, args);
    },
    delay);
  };
};

